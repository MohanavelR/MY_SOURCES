console.log("Variable   : var")
console.log("Introduced :1997")
var Var= 21;
console.log("Var Value  :",Var)
var Var=23;
console.log("Var re-declare Value :",Var)
console.log("var is access local variable (if else,)")
console.log('var is re-assign and re-declare')
console.log("===============================")
console.log("Variable   : let")
console.log("Introduced :ES version,2015")
let Let= 21;
console.log("let Value  :",Let)
Let=23;
console.log("let re-assign Value :",Let)
console.log("let is not allow access local variable (if else,)")
console.log('let is re-assign and not allow re-declare')
console.log("===============================")
