console.log("Relational Operactor: AND(&&)")
console.log("(AND)true&&true  :",true&&true)
console.log("(AND)true&&false :",true&&false)
console.log("(AND)false&&false :",false&&false)

console.log("Relational Operactor: OR(||)")
console.log("(OR)true||true  :",true||true)
console.log("(OR)true||false :",true||false)
console.log("(OR)false||false :",false||false)

console.log("Relational Operactor: XOR(^^)")
console.log("(OXR)true^true  :",true^true)
console.log("(OXR)true^false :",true^false)
console.log("(OXR)false^false :",false^false)

console.log("Relational Operactor: NOT(!)")
console.log("(NOT)!true  :",!true)
console.log("(NOT)!false :",!false)
