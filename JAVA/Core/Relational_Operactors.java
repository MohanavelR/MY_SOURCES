public class Relational_Operactors {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("Value a      :"+a);
        System.out.println("Value b      :"+b);
        System.out.println("Value a==b   :"+(a==b));
        System.out.println("Value a<b    :"+(a<b));
        System.out.println("Value a>b    :"+(a>b));
        System.out.println("Value a>=b   :"+(a>=b));
        System.out.println("Value a<=b   :"+(a<=b));    
        System.out.println("Value a!=b   :"+(a!=b));      
    }
    
}
