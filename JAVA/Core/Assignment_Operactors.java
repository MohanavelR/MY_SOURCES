public class Assignment_Operactors {
    public static void main(String[] args) {
        int a=1;
System.out.println("Value a     :"+a);
a=1;
System.out.println("Value a     :"+a);
a+=1;
System.out.println("Value a+=1  :"+a);
a=1;
System.out.println("Value a     :"+a);
a-=1;
System.out.println("Value a-=1  :"+a);
a=6;
System.out.println("Value a     :"+a);
a/=2;
System.out.println("Value a/=2  :"+a);
a=6;
System.out.println("Value a     :"+a);
a*=2;
System.out.println("Value a*=2  :"+a);
a=17;
System.out.println("Value a     :"+a);
a%=2;
System.out.println("Value a%=2  :"+a);
    }
}
