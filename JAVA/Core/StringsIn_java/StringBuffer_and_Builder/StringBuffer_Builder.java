package StringsIn_java.StringBuffer_and_Builder;

public class StringBuffer_Builder {
 public static void main(String[] args) {
    StringBuffer strbuffer=new StringBuffer("String Buffer");
    StringBuilder strbuilder=new StringBuilder("String Builder");
    System.out.println("String Buffer value  : "+strbuffer);
    System.out.println("String builder Value : "+strbuilder);
 }   
}
