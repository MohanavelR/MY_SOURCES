package StringsIn_java.strings_Methods;

public class ReplaceFirst {
    public static void main(String[] args) {
        String str="Web Development";
        System.out.println("String Function     :str.replaceFirst(\"Full Stack\")");
        System.out.println("My String str       : "+str);
        System.out.println("String Method Value : "+str.replaceFirst("Web","Full Stack"));
        System.out.println("===================================================");
        str="Web Development";
        System.out.println("String Function     :str.replaceFirst(\"e\",\"a\")");
        System.out.println("My String str       : "+str);
        System.out.println("String Method Value : "+str.replaceFirst("e","a"));
        System.out.println("===================================================");
    
    }
    }
    

