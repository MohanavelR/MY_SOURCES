package StringsIn_java.strings_Methods;

public class Contains {
    public static void main(String[] args) {
    String str="Web Development";
    System.out.println("String Function     :str.contains(\"e\")");
    System.out.println("My String str       : "+str);
    System.out.println("String Method Value : "+str.contains("e"));
    System.out.println("========================================");
    str="Web Development";
    System.out.println("String Function     :str.contains(\"z\")");
    System.out.println("My String str       : "+str);
    System.out.println("String Method Value : "+str.contains("z"));
    System.out.println("========================================");
    str="Web Development";
    System.out.println("String Function     :str.contains(\"Web\")");
    System.out.println("My String str       : "+str);
    System.out.println("String Method Value : "+str.contains("Web"));
    System.out.println("========================================");

}
}

