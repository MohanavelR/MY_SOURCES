package StringsIn_java.strings_Methods;

public class charAt {
    public static void main(String[] args) {
    String str="Web Development";
    System.out.println("String Function     :str.charAt(0)");
    System.out.println("My String str       : "+str);
    System.out.println("String Method Value : "+str.charAt(0));
    System.out.println("=============================================");
    str="Web Development";
    System.out.println("String Function     :str.charAt(4)");
    System.out.println("My String str       : "+str);
    System.out.println("String Method Value : "+str.charAt(4));
    
}
}
