package StringsIn_java.strings_Methods;

public class Concat {
 public static void main(String[] args) {
    String str="Web Development";
    System.out.println("String Function     :str+ in java");
    System.out.println("My String str       : "+str);
    System.out.println("String Method Value : "+str+" in java");
    System.out.println("==========================================");
    str="Web Development";
    System.out.println("String Function     :str.concat(' in java')");
    System.out.println("My String str       : "+str);
    System.out.println("String Method Value : "+str.concat(" in java"));
}   
}
