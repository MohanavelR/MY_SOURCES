
public class Logical_Operactors {
    @SuppressWarnings("unused")
    public static void main(String[] args) {
        System.out.println("Relational Operactor: AND(&&)");
System.out.println("(AND)true&&true   :"+(true&&true));
System.out.println("(AND)true&&false  :"+(true&&false));
System.out.println("(AND)false&&false  :"+(false&&false));

System.out.println("Relational Operactor: OR(||)");
System.out.println("(OR)true||true   :"+(true||true));
System.out.println("(OR)true||false  :"+(true||false));
System.out.println("(OR)false||false  :"+(false||false));

System.out.println("Relational Operactor: XOR(^^)");
System.out.println("(OXR)true^true   :"+(true^true));
System.out.println("(OXR)true^false  :"+(true^false));
System.out.println("(OXR)false^false  :"+(false^false));

System.out.println("Relational Operactor: NOT(!)");
System.out.println("(NOT)!true   :"+(!true));
System.out.println("(NOT)!false  :"+(!false));
    }

}
